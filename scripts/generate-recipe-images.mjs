/**
 * Generates a photo for every recipe via the OpenAI Images API (gpt-image-1)
 * into public/recipes/<slug>.webp, and always (re)writes
 * scripts/recipe-image-prompts.md so the prompts can instead be pasted into
 * ChatGPT by hand and the files dropped in.
 *
 * Local tool, not part of the build. Requires OPENAI_API_KEY (reads .env.local).
 *
 *   node scripts/generate-recipe-images.mjs [--only <slug>] [--force] [--dry-run]
 *
 * Existing files are skipped unless --force. After generating, add
 * `image: "/recipes/<slug>.webp"` to each recipe in src/data/recipes.ts -
 * the script prints the exact lines, and `npm run validate` enforces
 * field <-> file agreement.
 */
import { execSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outDir = join(root, "public", "recipes");
const promptsFile = join(root, "scripts", "recipe-image-prompts.md");

// recipes.ts is TypeScript - extract the fields we need via tsx
const recipes = JSON.parse(
  execSync(
    `npx tsx -e 'import { recipes } from "./src/data/recipes"; console.log(JSON.stringify(recipes.map(r => ({ slug: r.slug, name: r.name, mealType: r.mealType, ingredients: r.ingredients.slice(0, 5).map(i => i.item) }))))'`,
    { cwd: root, encoding: "utf8" },
  ),
);

const args = process.argv.slice(2);
const only = args.includes("--only") ? args[args.indexOf("--only") + 1] : null;
const force = args.includes("--force");
const dryRun = args.includes("--dry-run");

function loadApiKey() {
  if (process.env.OPENAI_API_KEY) return process.env.OPENAI_API_KEY;
  try {
    const env = readFileSync(join(root, ".env.local"), "utf8");
    const m = env.match(/^OPENAI_API_KEY=(.+)$/m);
    if (m) return m[1].trim();
  } catch {
    /* no .env.local */
  }
  return null;
}

function promptFor(r) {
  const mainIngredients = r.ingredients
    .filter((i) => !/salt|pepper|oil\b|seasoning|powder|cumin|paprika|oregano/i.test(i))
    .slice(0, 4)
    .join(", ");
  return (
    `Overhead food photography of ${r.name.toLowerCase()} (a ${r.mealType[0]} dish), ` +
    `featuring ${mainIngredients.toLowerCase()}. ` +
    `Served on a matte ceramic plate on a dark slate surface, warm golden side light, ` +
    `shallow depth of field, rustic minimal styling, rich appetizing colors, ` +
    `photorealistic, no text, no hands, no cutlery in motion.`
  );
}

// always refresh the manual-generation prompts file
const md = [
  "# Recipe image prompts",
  "",
  "Generated by `scripts/generate-recipe-images.mjs`. To create images by hand,",
  "paste a prompt into ChatGPT (or any image model), save the result as the",
  "target file below (landscape, ~1536x1024), then add the `image:` field to",
  "that recipe in `src/data/recipes.ts`. `npm run validate` checks the rest.",
  "",
  ...recipes.flatMap((r) => [
    `## ${r.name}`,
    "",
    `- **File:** \`public/recipes/${r.slug}.webp\``,
    `- **Data field:** \`image: "/recipes/${r.slug}.webp"\``,
    `- **Prompt:** ${promptFor(r)}`,
    "",
  ]),
].join("\n");
writeFileSync(promptsFile, md);
console.log(`wrote ${promptsFile}`);

const targets = recipes.filter((r) => !only || r.slug === only);
if (only && targets.length === 0) {
  console.error(`no recipe with slug "${only}"`);
  process.exit(1);
}

if (dryRun) {
  for (const r of targets) console.log(`[dry-run] ${r.slug}: ${promptFor(r)}`);
  process.exit(0);
}

const apiKey = loadApiKey();
if (!apiKey) {
  console.error("OPENAI_API_KEY not set (env or .env.local) - only wrote the prompts file.");
  process.exit(1);
}

mkdirSync(outDir, { recursive: true });
let generated = 0;
let skipped = 0;
const failures = [];

for (const r of targets) {
  const file = join(outDir, `${r.slug}.webp`);
  if (existsSync(file) && !force) {
    skipped++;
    continue;
  }
  process.stdout.write(`generating ${r.slug} ... `);
  try {
    const res = await fetch("https://api.openai.com/v1/images/generations", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-image-1",
        prompt: promptFor(r),
        size: "1536x1024",
        quality: "medium",
        output_format: "webp",
      }),
    });
    if (!res.ok) {
      const body = await res.text();
      throw new Error(`${res.status} ${body.slice(0, 300)}`);
    }
    const data = await res.json();
    const b64 = data.data?.[0]?.b64_json;
    if (!b64) throw new Error("no b64_json in response");
    writeFileSync(file, Buffer.from(b64, "base64"));
    generated++;
    console.log("ok");
  } catch (err) {
    failures.push(r.slug);
    console.log(`FAILED: ${err.message}`);
  }
}

console.log(`\n${generated} generated, ${skipped} skipped (exists), ${failures.length} failed`);
if (failures.length) console.log(`failed: ${failures.join(", ")} - re-run to retry`);
if (generated) {
  console.log("\nAdd to src/data/recipes.ts (validator will hold you to it):");
  for (const r of targets) {
    if (existsSync(join(outDir, `${r.slug}.webp`)))
      console.log(`  ${r.slug}: image: "/recipes/${r.slug}.webp",`);
  }
}
